    Constants.prototype.FindBucket = function (insertsize, type, chemistry, celltype, sizeselection) {

        var result, parent, parentprop, prop, size, item, low, high, i, j;

        size = parseInt(insertsize, 10);
        for (i = 0; i < this.buckets.length; i += 1) {
            item = this.buckets[i];
            if (type !== item.Type) { continue; }
            if (chemistry !== item.Chemistry) { continue; }
            if (celltype !== item.CellType) { continue; }
            if (sizeselection !== item.SizeSelection) { continue; }

            low = parseInt(item.LowSize, 10);
            high = parseInt(item.HighSize, 10);

            if ((size >= low) && (size <= high)) {

                //
                // parent bucket support
                //
                // if the bucket we've found has a parent, then
                // inherit the parent's properties and overlay
                // this bucket's properties. Single level simple
                // inheritence to simplify the buckets above
                //

                if (item.hasOwnProperty("Parent")) {
                    result = {};

                    // find and copy the properties of the parent into result
                    for (j = 0; j < this.buckets.length; j += 1) {
                        parent = this.buckets[j];
                        if (parent.Name !== item.Parent) {
                            continue;
                        }

                        for (parentprop in parent) {
                            if (parent.hasOwnProperty(parentprop)) {
                                result[parentprop] = parent[parentprop];
                            }
                        }
                    }

                    // then copy the properties of the bucket into result
                    for (prop in item) {
                        if (item.hasOwnProperty(prop)) {
                            result[prop] = item[prop];
                        }
                    }

                    return result;
                }

                return item;
            }
        }

        return undefined;
    };

//
// returns a list of all valid bucket sizes for a given chemistry type
// used by the print view for grouping buckets for dilution calculations
//
Constants.prototype.BucketSizes = function (chemistry) {
    var i, item, value, result = [];

    for (i = 0; i < this.buckets.length; i += 1) {
        item = this.buckets[i];
        if (chemistry !== item.Chemistry) { continue; }
        value = item.Size;

        // if we haven't already seen this bucket size, 
        // add it to the result.
        if (-1 === $.inArray(value, result)) {
            result.push(value);
        }
    }
    return result;
};

