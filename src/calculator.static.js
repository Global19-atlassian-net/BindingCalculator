Constants.prototype.ReplaceGlobalStrings = function () {

    var item, that = this,
        stringlist = this.strings;

    // return a new function with closure for jslint
    function make_textreplacer(theitem) {
        return function () {
            $(this).text(that.strings[theitem]);
        };
    }

    for (item in stringlist) {
        if (stringlist.hasOwnProperty(item)) {
            $('.STRINGS_' + item).each(make_textreplacer(item));
        }
    }
};

